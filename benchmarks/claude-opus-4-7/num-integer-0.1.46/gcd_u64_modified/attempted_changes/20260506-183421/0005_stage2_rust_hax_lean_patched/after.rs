// Monomorphic u64 version of `num_integer::gcd` from num-integer 0.1.46.
//
// Source: src/lib.rs:420-422 dispatches `pub fn gcd<T: Integer>(x: T, y: T) -> T`
// to the trait method, which for unsigned integers is implemented at
// src/lib.rs:868-895 via Stein's binary GCD algorithm.
//
// The free function and the trait method body are merged into one
// concrete `gcd: (u64, u64) -> u64`.
//
// ----------------------------------------------------------------------------
// Hax-compatibility rewrites (semantics unchanged, behaviour-equivalent on every
// (u64, u64) input — covered by the existing exhaustive 0..=255 sweep test and
// the property tests further down):
//
//   * `u64::trailing_zeros` is not modelled in `core_models` for u64 in the
//     current Hax prelude (`Unknown identifier core_models.num.Impl_9.trailing_zeros`
//     at lake-build time).  Each call is inlined as a `while value & 1 == 0`
//     right-shift loop.  The pre-loop guard `m == 0 || n == 0` (which early-
//     returns) means every value passed to a `value.trailing_zeros()` call in
//     the original code is non-zero, so the inlined loop both terminates and
//     produces the same final `value` that the original `value >>= value.trailing_zeros()`
//     idiom produced.
//
//   * Each `while` loop carries `hax_lib::loop_decreases!(<measure>)` so that
//     the extracted Lean uses a real termination measure rather than the
//     constant 0 that Hax otherwise inserts (which makes the downstream
//     `rust_primitives.hax.while_loop.spec` lemma unusable).
//
//   * No `loop_invariant!` is supplied here: a useful invariant for Stein's
//     algorithm involves bitwise / oddness predicates that are known to
//     trip the `pureP` synthesis at extraction.  The proof stage will
//     supply a Lean-side invariant via `Spec.MonoLoopCombinator.while_loop`.

pub fn gcd(x: u64, y: u64) -> u64 {
    // Use Stein's algorithm
    let mut m = x;
    let mut n = y;
    if m == 0 || n == 0 {
        return m | n;
    }

    // From here on, both `m > 0` and `n > 0`.

    // Find the largest `k` such that 2^k divides both `m` and `n`,
    // accumulated in `shift`.  Equivalent to the original
    // `let shift = (m | n).trailing_zeros();` followed by halving by
    // 2^shift, but expressed as a primitive loop so Hax does not need
    // `u64::trailing_zeros`.
    let mut shift: u32 = 0;
    while (m | n) & 1 == 0 {
        hax_lib::loop_decreases!(m | n);
        m >>= 1;
        n >>= 1;
        shift += 1;
    }

    // Strip any remaining factors of 2 from `m` so it becomes odd.
    // Equivalent to `m >>= m.trailing_zeros();` (with the original `m`
    // still non-zero, guaranteed by the early-return guard).
    while m & 1 == 0 {
        hax_lib::loop_decreases!(m);
        m >>= 1;
    }
    // Strip any remaining factors of 2 from `n` so it becomes odd.
    while n & 1 == 0 {
        hax_lib::loop_decreases!(n);
        n >>= 1;
    }

    // Both `m` and `n` are now odd and positive.  Each outer iteration
    // strictly decreases `m + n`: the larger value is replaced by the
    // (positive) difference, and then any new factors of 2 are stripped,
    // which can only further reduce it.
    while m != n {
        // Termination measure: bitwise OR `m | n` strictly decreases each
        // iteration.  In the `m > n` branch, m_new = (m - n) >> k for some
        // k >= 1 (since m - n is even — both odd before), so
        //   m_new < m - n < m   ⇒   m_new + n < m ≤ m | n
        // and `m_new | n_new <= m_new + n < m | n`.  The `m < n` branch is
        // symmetric.  Bitwise OR has no overflow check at extraction time,
        // unlike `m + n`, which trips Hax's `pureP` synthesis on u64
        // (`UInt64.addOverflow` may be true).
        hax_lib::loop_decreases!(m | n);
        if m > n {
            m -= n;
            // `m` is now even and non-zero (odd - odd = even, and positive
            // since `m > n > 0`); strip its trailing zeros.
            while m & 1 == 0 {
                hax_lib::loop_decreases!(m);
                m >>= 1;
            }
        } else {
            n -= m;
            // `n` is now even and non-zero (odd - odd = even, and positive
            // since `n > m > 0`); strip its trailing zeros.
            while n & 1 == 0 {
                hax_lib::loop_decreases!(n);
                n >>= 1;
            }
        }
    }
    m << shift
}

#[cfg(test)]
mod tests {
    // Tests transferred from num-integer 0.1.46 src/lib.rs (the tests
    // generated by `impl_integer_for_usize!(u64, ...)` at src/lib.rs:974-998).
    // `(N as $T).gcd(&M)` is rewritten to `gcd(N, M)`; otherwise the
    // bodies and assertions are unchanged.

    use super::*;
    use core::mem;

    #[test]
    fn test_gcd() {
        assert_eq!(gcd(10, 2), 2);
        assert_eq!(gcd(10, 3), 1);
        assert_eq!(gcd(0, 3), 3);
        assert_eq!(gcd(3, 3), 3);
        assert_eq!(gcd(56, 42), 14);
    }

    #[test]
    fn test_gcd_cmp_with_euclidean() {
        fn euclidean_gcd(mut m: u64, mut n: u64) -> u64 {
            while m != 0 {
                mem::swap(&mut m, &mut n);
                m %= n;
            }
            n
        }

        for i in 0..=255u64 {
            for j in 0..=255u64 {
                assert_eq!(euclidean_gcd(i, j), gcd(i, j));
            }
        }
    }

    // ---------- Property-based tests capturing the contract ----------
    //
    // Contract for `gcd: (u64, u64) -> u64`:
    //   * Preconditions: none (every (u64, u64) is valid input).
    //   * Failure conditions: none (the function is total; no panics,
    //     and the result is bounded by max(x, y) so `m << shift` cannot
    //     overflow).
    //   * Postconditions:
    //       (Z) Zero boundary: gcd(0, 0) = 0, gcd(x, 0) = gcd(0, x) = x.
    //       (D) The result divides both inputs (it is a common divisor).
    //       (G) The result is the greatest such divisor: every common
    //           divisor of the inputs divides the result.
    // (Z), (D), (G) together uniquely characterise the GCD on u64.

    /// Postcondition (Z): zero boundary cases.
    ///
    /// Distinguishes the convention `gcd(0, 0) = 0` and the identity
    /// behaviour `gcd(x, 0) = gcd(0, x) = x`, which the implementation
    /// realises via `m | n` on the early-return path.
    #[test]
    fn prop_gcd_zero_cases() {
        // gcd(0, 0) = 0
        assert_eq!(gcd(0, 0), 0);
        // gcd(x, 0) = x and gcd(0, x) = x
        for x in 0..=255u64 {
            assert_eq!(gcd(x, 0), x);
            assert_eq!(gcd(0, x), x);
        }
        // Spot-check the upper end of the range to guard against
        // surprises in `m | n` on large values.
        assert_eq!(gcd(u64::MAX, 0), u64::MAX);
        assert_eq!(gcd(0, u64::MAX), u64::MAX);
    }

    /// Postcondition (D): `gcd(x, y)` is a common divisor of `x` and `y`.
    ///
    /// When `g == 0` we additionally require `x == 0 && y == 0`, since
    /// otherwise `g` would not be a divisor of a nonzero value.
    #[test]
    fn prop_gcd_divides_both() {
        for x in 0..=128u64 {
            for y in 0..=128u64 {
                let g = gcd(x, y);
                if g == 0 {
                    // The only way `g` can be 0 is the (0, 0) case.
                    assert_eq!(x, 0);
                    assert_eq!(y, 0);
                } else {
                    assert_eq!(x % g, 0, "gcd({}, {}) = {} does not divide x", x, y, g);
                    assert_eq!(y % g, 0, "gcd({}, {}) = {} does not divide y", x, y, g);
                }
            }
        }
    }

    /// Postcondition (G): every common divisor of `x` and `y` divides
    /// `gcd(x, y)`.
    ///
    /// This is independent of (D): an implementation that always
    /// returned `1` would satisfy (D) but fail (G). Conversely, an
    /// implementation returning a strict multiple of the true GCD could
    /// satisfy (G) but fail (D).
    #[test]
    fn prop_gcd_is_greatest() {
        for x in 0..=64u64 {
            for y in 0..=64u64 {
                let g = gcd(x, y);
                // Any common divisor `d` of x and y with x, y <= 64 lies
                // in 1..=64, so this range is exhaustive over candidate
                // common divisors.
                for d in 1..=64u64 {
                    if x % d == 0 && y % d == 0 {
                        // `d` is a common divisor, so it must divide `g`.
                        // When g == 0 we have x = y = 0 and 0 % d == 0,
                        // which is consistent with the convention.
                        assert_eq!(
                            g % d,
                            0,
                            "common divisor d = {} of x = {}, y = {} does not divide gcd = {}",
                            d, x, y, g,
                        );
                    }
                }
            }
        }
    }
}
