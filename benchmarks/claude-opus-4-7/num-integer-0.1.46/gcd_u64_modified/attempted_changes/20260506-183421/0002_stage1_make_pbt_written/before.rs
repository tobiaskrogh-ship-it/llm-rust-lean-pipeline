// Monomorphic u64 version of `num_integer::gcd` from num-integer 0.1.46.
//
// Source: src/lib.rs:420-422 dispatches `pub fn gcd<T: Integer>(x: T, y: T) -> T`
// to the trait method, which for unsigned integers is implemented at
// src/lib.rs:868-895 via Stein's binary GCD algorithm.
//
// The free function and the trait method body are merged into one
// concrete `gcd: (u64, u64) -> u64`.

pub fn gcd(x: u64, y: u64) -> u64 {
    // Use Stein's algorithm
    let mut m = x;
    let mut n = y;
    if m == 0 || n == 0 {
        return m | n;
    }

    // find common factors of 2
    let shift = (m | n).trailing_zeros();

    // divide n and m by 2 until odd
    m >>= m.trailing_zeros();
    n >>= n.trailing_zeros();

    while m != n {
        if m > n {
            m -= n;
            m >>= m.trailing_zeros();
        } else {
            n -= m;
            n >>= n.trailing_zeros();
        }
    }
    m << shift
}

#[cfg(test)]
mod tests {
    // Tests transferred from num-integer 0.1.46 src/lib.rs (the tests
    // generated by `impl_integer_for_usize!(u64, ...)` at src/lib.rs:974-998).
    // `(N as $T).gcd(&M)` is rewritten to `gcd(N, M)`; otherwise the
    // bodies and assertions are unchanged.

    use super::*;
    use core::mem;

    #[test]
    fn test_gcd() {
        assert_eq!(gcd(10, 2), 2);
        assert_eq!(gcd(10, 3), 1);
        assert_eq!(gcd(0, 3), 3);
        assert_eq!(gcd(3, 3), 3);
        assert_eq!(gcd(56, 42), 14);
    }

    #[test]
    fn test_gcd_cmp_with_euclidean() {
        fn euclidean_gcd(mut m: u64, mut n: u64) -> u64 {
            while m != 0 {
                mem::swap(&mut m, &mut n);
                m %= n;
            }
            n
        }

        for i in 0..=255u64 {
            for j in 0..=255u64 {
                assert_eq!(euclidean_gcd(i, j), gcd(i, j));
            }
        }
    }
}
